    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userName = dangNhap.getText();
        String matKhau = password.getText();
        String reMatKhau = rePassword.getText();
        
        try {
            if(!(matKhau.equals(reMatKhau))){
                JOptionPane.showMessageDialog(this, "Mật khẩu không khớp !!!");
            }
            else{
                Connection conn = null;
            conn = (Connection) DoAn_CuaHangDaQuy.getMyConnection();
            String SQLKT = "SELECT SQL from SQL WHERE SQL = ?";
            PreparedStatement pskt = conn.prepareStatement(SQLKT);
            pskt.setString(1, userName);
            ResultSet rs = pskt.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Tài khoản đã tồn tại !!!");
            } else {
                String SQL = "INSERT INTO SQL VALUES(?, ?)";
                PreparedStatement ps = conn.prepareStatement(SQL);
                ps.setString(1, userName);
                ps.setString(2, matKhau);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Đăng ký thành công !!!");
            }
            conn.close();
            }
        } catch (SQLException ex) {
            System.out.println(ex);
            System.out.println("Xảy ra lỗi!");
        } catch (ClassNotFoundException cx) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

