    private void bang1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bang1MouseClicked
        // TODO add your handling code here:
        int Index = bang1.getSelectedRow();
        if (Index < tableDanhSachDoi.getRowCount() && Index >= 0) {
            maDoiTac.setText(tableDanhSachDoi.getValueAt(Index, 0).toString());
            tenCongTy.setText(tableDanhSachDoi.getValueAt(Index, 1).toString());
            diaChi.setText(tableDanhSachDoi.getValueAt(Index, 2).toString());
            soDienThoai.setText(tableDanhSachDoi.getValueAt(Index, 3).toString());
        }

    }//GEN-LAST:event_bang1MouseClicked

